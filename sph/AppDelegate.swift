extension UIViewController {

    func showSnackBar(message: String) {
        let snackbar = TTGSnackbar(
            message: "   " + message,
            duration: .forever,
            actionText: "Close!",
            actionBlock: { (snackbar) in
                snackbar.dismiss()
            }
        )
        snackbar.show()
    }
}

extension Results {
    func toArray<T>(ofType: T.Type) -> [T] {
        var array = [T]()
        for i in 0 ..< count {
            if let result = self[i] as? T {
                array.append(result)
            }
        }

        return array
    }
}
